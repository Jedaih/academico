#Crie um programa que receba dois números, e mostre na tela, o resultado de todas as operações matemáticas.

n1 = int(input('Digite um número: '))
n2 = int(input('Digite outro número: '))
print(f'a soma é {n1 + n2}')
print(f'a subtração é {n1 - n2}')
print(f'a multiplicação é {n1 * n2}')
print(f'a divisão é {n1 / n2}')
print(f'o resto da divisão entre eles é {n1 % n2}')